    /**
     * 转换成指定类型
     *
     * @param obj   源
     * @param clazz 目标类型
     * @param <T>   目标类型
     * @return 目标类型
     */
    @SuppressWarnings("unchecked")
    public static <T> T parse(Object obj, Class<T> clazz) {
        if (null != obj && StringUtils.isNotEmpty(String.valueOf(obj))) {
            if (clazz.equals(Integer.class) || clazz.equals(int.class)) {
                return (T) Integer.valueOf(obj.toString());
            } else if (clazz.equals(Long.class) || clazz.equals(long.class)) {
                return (T) Long.valueOf(obj.toString());
            } else if (clazz.equals(Boolean.class) || clazz.equals(boolean.class)) {
                return (T) Boolean.valueOf(obj.toString());
            } else if (clazz.equals(Short.class) || clazz.equals(short.class)) {
                return (T) Short.valueOf(obj.toString());
            } else if (clazz.equals(Float.class) || clazz.equals(float.class)) {
                return (T) Float.valueOf(obj.toString());
            } else if (clazz.equals(Double.class) || clazz.equals(double.class)) {
                return (T) Double.valueOf(obj.toString());
            } else if (clazz.equals(Byte.class) || clazz.equals(byte.class)) {
                return (T) Byte.valueOf(obj.toString());
            } else if (clazz.equals(Character.class) || clazz.equals(char.class)) {
                return (T) Character.valueOf(obj.toString().charAt(0));
            } else if (clazz.equals(String.class)) {
                return (T) String.valueOf(obj);
            } else if (clazz.equals(BigDecimal.class)) {
                return (T) new BigDecimal(obj.toString());
            } else if (clazz.equals(Date.class)) {
                if (obj instanceof Date) {
                    return (T) obj;
                }
                return (T)DateUtils.parseDate(obj);
            }
        }
        return null;
    }

    /**
     * 判断字符串是不是double型
     *
     * @param str
     * @return
     */
    @SuppressWarnings("unchecked")
    public static boolean isDouble(String str) {

        Pattern pattern = Pattern.compile("[0-9]+[.]{0,1}[0-9]*[dD]{0,1}");

        Matcher isNum = pattern.matcher(str);

        if (!isNum.matches()) {

            return false;

        }

        return true;

    }
